## Use the cached (stored in memory) inverse of a matrix if it already exists. If not,
##  calculate the inverse of the matrix and store it in memory.

## Create a list of functions to get and set the inverse of a passed matrix.

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) m <<- solve
  getsolve <- function() m
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}


## Use the cached value of the inverse of an already set inverse of a matrix unless
##  the inverse of the passed matrix has not been calculated and stored.

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getsolve()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setsolve(m)
  m
}
